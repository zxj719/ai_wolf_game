export function normalizeLocale(locale) {
  return SUPPORTED_LOCALES.includes(locale) ? locale : DEFAULT_LOCALE;
}

export function readStoredLocale() {
  if (typeof window === 'undefined' || !window.localStorage) {
    return DEFAULT_LOCALE;
  }

  const params = new URLSearchParams(window.location.search || '');
  const requested = params.get('lang');
  if (SUPPORTED_LOCALES.includes(requested)) {
    return requested;
  }

  const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  if (SUPPORTED_LOCALES.includes(stored)) {
    return stored;
  }

  const legacyStored = window.localStorage.getItem(LEGACY_THINKING_LIBRARY_LOCALE_STORAGE_KEY);
  if (SUPPORTED_LOCALES.includes(legacyStored)) {
    return legacyStored;
  }

  return DEFAULT_LOCALE;
}

export function writeStoredLocale(locale) {
  if (typeof window === 'undefined' || !window.localStorage) {
    return;
  }
  window.localStorage.setItem(LOCALE_STORAGE_KEY, normalizeLocale(locale));
  window.localStorage.removeItem(LEGACY_THINKING_LIBRARY_LOCALE_STORAGE_KEY);
}

export function getUiCopy(locale = DEFAULT_LOCALE) {
  return UI_COPY[normalizeLocale(locale)];
}

export function resolveRoleKey(roleOrKey) {
  if (!roleOrKey) return null;
  if (ROLE_COPY[roleOrKey]) return roleOrKey;
  return ROLE_KEY_BY_VALUE[roleOrKey] || null;
}

export function getRoleLabel(roleOrKey, locale = DEFAULT_LOCALE) {
  const roleKey = resolveRoleKey(roleOrKey);
  if (!roleKey) return roleOrKey;
  return ROLE_COPY[roleKey][normalizeLocale(locale)].label;
}

export function getShortRoleLabel(roleOrKey, locale = DEFAULT_LOCALE) {
  const roleKey = resolveRoleKey(roleOrKey);
  if (!roleKey) return roleOrKey;
  return ROLE_COPY[roleKey][normalizeLocale(locale)].shortLabel;
}

export function getRoleDescription(roleKey, locale = DEFAULT_LOCALE) {
  const resolvedRoleKey = resolveRoleKey(roleKey);
  if (!resolvedRoleKey) return '';
  return ROLE_COPY[resolvedRoleKey][normalizeLocale(locale)].description;
}

export function formatRoleSummary(selections, locale = DEFAULT_LOCALE) {
  const order = ['WEREWOLF', 'VILLAGER', 'SEER', 'WITCH', 'HUNTER', 'GUARD', 'MAGICIAN', 'KNIGHT', 'DREAMWEAVER'];
  return order
    .filter((roleKey) => (selections?.[roleKey] || 0) > 0)
    .map((roleKey) => `${selections[roleKey]} ${getShortRoleLabel(roleKey, locale)}`)
    .join(' ');
}

export function getVictoryModeCopy(mode, locale = DEFAULT_LOCALE) {
  return VICTORY_MODE_COPY[mode]?.[normalizeLocale(locale)] || VICTORY_MODE_COPY.edge.zh;
}
